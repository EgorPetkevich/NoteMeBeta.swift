import Foundation
import UIKit

extension UIColor {
    
    static func white() -> CGColor {
        return UIColor(red: 0.949, green: 0.949, blue: 0.949, alpha: 1).cgColor
    }
    static func black() -> CGColor {
        return UIColor(red: 0.158, green: 0.158, blue: 0.158, alpha: 1).cgColor
    }
    static func yellow() -> CGColor {
        return UIColor(red: 1, green: 0.91, blue: 0.1, alpha: 1).cgColor
    }
    static func red() -> CGColor {
        return UIColor(red: 0.946, green: 0.248, blue: 0.248, alpha: 1).cgColor
    }
    
}
